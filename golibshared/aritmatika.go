package golibshared

//Penjumlahan function for handling penjumlahan
func Penjumlahan(a, b int) int {
	return a + b
}

//Pengurangan function for handling pengurangN
func Pengurangan(a, b int) int {
	return a - b
}

//Pembagian function for handling pembagian
func Pembagian(a, b int) float64 {
	return float64(a) / float64(b)
}
